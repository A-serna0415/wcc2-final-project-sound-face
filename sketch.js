// The web font can load after setup, so I mark when it is actually available for the canvas text.
function setupHudFont() {
  if (!document.fonts) {
    // If the browser does not support this API, the fallback font stays active.
    return;
  }

  // This tells the sketch when the custom HUD font is really available.
  document.fonts.load(`16px '${HUD_FONT}'`).then(() => {
    appState.hudFontReady = true;
  });
}

